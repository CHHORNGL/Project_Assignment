def _contains_khmer(text: str) -> bool:
    return any("\u1780" <= ch <= "\u17FF" for ch in text)


def _looks_like_khmer_mojibake(text: str) -> bool:
    if not text:
        return False
    if _contains_khmer(text):
        return False
    return any(marker in text for marker in MOJIBAKE_HINTS)


def _decode_khmer_mojibake(text: str) -> str:
    # Some strings are double-encoded, so decode up to a few passes.
    current = text
    for _ in range(3):
        raw_bytes = bytearray()
        for char in current:
            codepoint = ord(char)
            if codepoint <= 0xFF:
                raw_bytes.append(codepoint)
                continue
            mapped = CP1252_REVERSE_MAP.get(codepoint)
            if mapped is None:
                return text
            raw_bytes.append(mapped)
        try:
            candidate = bytes(raw_bytes).decode("utf-8")
        except UnicodeError:
            return text
        if _contains_khmer(candidate):
            return candidate
        if candidate == current or not _looks_like_khmer_mojibake(candidate):
            return text
        current = candidate
    return text


def normalize_display_text(value, lang: str | None = None):
    """
    Fix Khmer mojibake text (UTF-8 decoded as cp1252/latin-1) at render time.
    """
    if value is None or not isinstance(value, str):
        return value
    if not _looks_like_khmer_mojibake(value):
        return value

    active_lang = (lang or get_current_language() or DEFAULT_LANGUAGE).strip().lower()
    if active_lang != "km":
        return value

    fixed = _decode_khmer_mojibake(value)
    if fixed == value:
        return value
    if isinstance(value, Markup):
        return Markup(fixed)
    return fixed


def get_current_language() -> str:
    if hasattr(g, "site_language"):
        return g.site_language
    lang = DEFAULT_LANGUAGE
    try:
        setting = SiteSetting.query.filter_by(key=LANGUAGE_SETTING_KEY).first()
        if setting and setting.value in TRANSLATIONS:
            lang = setting.value
    except Exception:
        lang = DEFAULT_LANGUAGE
    g.site_language = lang
    return lang


def set_current_language(lang: str) -> str:
    lang = (lang or "").strip().lower()
    if lang not in TRANSLATIONS:
        lang = DEFAULT_LANGUAGE
    setting = SiteSetting.query.filter_by(key=LANGUAGE_SETTING_KEY).first()
    if not setting:
        setting = SiteSetting(key=LANGUAGE_SETTING_KEY, value=lang)
        db.session.add(setting)
    else:
        setting.value = lang
    db.session.commit()
    return lang


def t(key: str, **kwargs) -> str:
    lang = get_current_language()
    text = TRANSLATIONS.get(lang, {}).get(key)
    if text is None:
        text = TRANSLATIONS.get(DEFAULT_LANGUAGE, {}).get(key, key)
    text = normalize_display_text(text, lang=lang)
    if kwargs:
        try:
            text = text.format(**kwargs)
        except Exception:
            pass
    return normalize_display_text(text, lang=lang)


